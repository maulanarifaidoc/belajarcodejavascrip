//membuat operasi function tambah, kurang, kali, bagi

//operasi penjumlahan
function tambah(a, b){
    return a + b;
}

//operasi pengurangan
function kurang(a, b){
    return a - b;
}

//operasi perkalian
function kali(a, b){
    return a * b;
}

//operasi pembagian
function bagi(a,b) {
    return a / b;
}

console.log(tambah(3, 5), kali (6, 4))